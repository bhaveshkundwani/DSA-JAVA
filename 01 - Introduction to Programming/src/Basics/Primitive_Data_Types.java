package Basics;

// Java program to demonstrate primitive data types
public class Primitive_Data_Types {
    public static void main(String[] args) {
//          1. boolean data type
        boolean b1 = true;
        boolean b2 = false;

        System.out.println("Is Java fun? " + b1);
        System.out.println("Is coding boring? " + b2);
        System.out.println(); // for new line

//         2. byte data type
        byte age = 25;
        byte tem = -10;

        System.out.println("Age: " + age);
        System.out.println("Temperature: " + tem);
        System.out.println();

//        3. short data type
        short num = 1000;
        short tm = -3;

        System.out.println("Number of Students: " + num);
        System.out.println("Temperature: " + tm);
        System.out.println();

//        4. int data type
        int p = 2000000;
        int neg = -150000;

        System.out.println("Population: " + p);
        System.out.println("Negative number: " + neg);
        System.out.println();

//        5. long data type
        long w = 7800000000L;

        System.out.println("World Population: " + w);
        System.out.println();

//        6. float data type
        float pi = 3.14f;

        System.out.println("Value of Pi: " + pi);
        System.out.println();

//        7. double data type
        double pie = 3.141592653589793;

        System.out.println("Value of Pi: " + pie);
        System.out.println();

//        8. char data type
        char grade = 'A';

        System.out.println("Grade: " + grade);
    }
}
