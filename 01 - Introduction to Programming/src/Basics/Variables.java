package Basics;

public class Variables {
    public static void main(String[] args) {
        // Integer variable
        int age = 25;

        // String variable
        String name = "Virat";

        // Double variable
        double salary = 50000.50;

        // Displaying the values of variables
        System.out.println("Age: " + age);
        System.out.println("Name: " + name);
        System.out.println("Salary: " + salary);
    }
}
